public class Point {

    private float x, y;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public float getX() {
        return x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public float getY() {
        return y;
    }
}
